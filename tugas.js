var body = document.getElementsByTagName('body')[0]

var bulet1 = document.getElementsByClassName('bulet1')[0]
bulet1.addEventListener('click',function() {
    body.style.backgroundColor = 'lightblue'
    bulet1.style.border = '1px solid lightblue'
})
bulet1.addEventListener('mouseenter',function() {
    bulet1.style.backgroundColor = 'lightblue'
    bulet1.style.border= "1px solid black"
    bulet1.style.opacity= 0.5
})
bulet1.addEventListener('mouseleave',function(){
    bulet1.style.backgroundColor = 'lightblue'
    bulet1.style.border= "1px solid #dedede"
    bulet1.style.opacity= 1
})
